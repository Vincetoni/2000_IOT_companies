function renderList(data) {
  listContainer.innerHTML = "";
  const fragment = document.createDocumentFragment();

  if (data.length === 0) {
    const empty = document.createElement("div");
    empty.className = "card";
    empty.innerHTML = "<h3>No matches</h3><p>Try a different keyword or category.</p>";
    fragment.appendChild(empty);
  }

  data.forEach(company => {
    const card = document.createElement("div");
    card.className = "card";

    const name = document.createElement("h3");
    name.textContent = company.name;

    const category = document.createElement("p");
    category.textContent = "Category: " + company.category;

    card.appendChild(name);
    card.appendChild(category);
    fragment.appendChild(card);
  });
  listContainer.appendChild(fragment);
  resultsMeta.textContent = `${data.length} of ${completeIotList.length} companies shown`;
}


renderList(completeIotList);



function applyFilters() {
  const keyword = searchInput.value.toLowerCase();
  const selectedCategory = select.value;

  const filtered = completeIotList.filter(company => {
    const matchesName = company.name.toLowerCase().includes(keyword);
    const matchesCategory = selectedCategory === "" || company.category === selectedCategory;

    return matchesName && matchesCategory;
  });

  renderList(filtered);
}

searchInput.addEventListener("input", applyFilters);
select.addEventListener("change", applyFilters);
